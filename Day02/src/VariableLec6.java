

public class VariableLec6 {
	public static void main(String args[]) {
		// Char 데이터 타입 다루기
		char alphaNum = 65; //--대문자 A
		char alpha = 'A' + 10; //-문자를 표현할때는 싱글 쿼테이션''
						  // -- "" 더블 쿼테이션은 문자열에만 사용
		
		System.out.println(alphaNum); // A 출력 
		System.out.println(alpha); // A 출력 / +10일때, K 출력
		
		//--중요한점은 char형은 숫자로 입력하더라도 문자열로 맵핑한다.
		
		
	}

}
